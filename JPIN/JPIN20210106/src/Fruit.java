
public class Fruit {
	private String fruit;
	private int price;

	Fruit(String fruit, int price){
		this.fruit = fruit;
		this.price = price;
	}

	public String getFruit() {
		return fruit;
	}

	public void setFruit(String fruit) {
		this.fruit = fruit;
	}

	public int getPrice() {
		return price;
	}

	public void setPrice(int price) {
		this.price = price;
	}

}
